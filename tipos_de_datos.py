#Tipos de datos básicos en Python

#Entero
#Representan números enteros sin parte decimal.
entero = 42

#Float
#Representan números con parte decimal.
float = 3.14

#Cadenas o string
#Representan secuencias de caracteres.
#Pueden definirse con comillas simples o dobles.
cadena = "Esto es un texto"

#Booleano
#Representan valores de verdad, True (verdadero) o False (falso).
booleano = True

#Listas
#Son secuencias mutables de elementos.
#Se definen con corchetes [].
lista = [1,2,3,4,5,6,7]

#Tuplas
#Son secuencias inmutables de elementos.
#Se definen con paréntesis ().
tupla = (1,2,3)

#Conjunto
#Colecciones no ordenadas de elementos únicos.
#Se definen con llaves {}.
conjunto = {1,2,3,4}

#Diccionario
#Colecciones de pares clave-valor.
#Se definen con llaves {} y cada elemento tiene la forma clave: valor.
diccionario = {'clave1': 'valor1', 'clave2': 'valor2'}

#Null o None
nulo = None
